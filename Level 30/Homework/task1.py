#1 სტრიქონის "hello world"-ის გათვალისწინებით, გამოიყენეთ kapitalize() მეთოდი მის შესაცვლელად.
user = input("enter words: ")
print(user.capitalize())

#2 გადააკეთეთ სტრიქონი „პითონის პროგრამირება“ ყველა დიდ ასოზე შესაბამისი მეთოდის გამოყენებით.
name=input("enter youre name: ")
print(name.upper())

#3 "HELLO WORLD" სტრიქონის გათვალისწინებით, გადააკეთეთ იგი ყველა პატარა ასოზე.
words=input("enter words: ")
print(words.lower())

#4 იპოვეთ ქვესტრინგის "world" ინდექსი "hello world" find() მეთოდის გამოყენებით.
print("hello world".find("world"))


#5 სტრიქონის „მონაცემთა მეცნიერების“ გათვალისწინებით, დაწერეთ იგი დიდი ასოებით.
print("sience".upper())


#6 გადააკეთეთ „მანქანური სწავლება“ პატარაზე.
print("Machine Learning".lower())

#7 გამოიყენეთ upper() მეთოდი „ღრმა სწავლის“ დიდზე გადასაყვანად.
print('deep learning'.upper())

#8 იპოვეთ „სწავლის“ პოზიცია სტრიქონში „ღრმა სწავლება“ find() გამოყენებით.
text = "deep learning"
position = text.find("learning")
print(position)

#9 „გამარჯობა“-ს მინიჭებით, გადააკეთეთ იგი დიდზე და იპოვეთ „L“-ის ინდექსი
name="hello"
print(name.upper().find("L"))

#10 სტრიქონისთვის „დილა მშვიდობისა“, ჩაწერეთ იგი და იპოვეთ „დილა“ ინდექსი.
