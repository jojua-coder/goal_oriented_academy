#4 MakeUpperCase

def make_upper_case(s):

    return s.upper()

#5 Convert boolean values to strings 'Yes' or 'No'
def bool_to_word(boolean):
    if boolean ==True:
        return "Yes"
    elif boolean == False:
        return "No"

#6 Remove First and Last Character
def remove_char(s):
    return s[1:-1]

